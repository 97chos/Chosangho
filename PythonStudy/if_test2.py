def is_leap_year(a):
    """연도를 입력받아 윤년 여부를 판단하는 함수"""
    if a%4==0 and a%100!=0 :
        result = "윤년입니다."
    elif a%400==0 :
        result = "윤년입니다."
    else :
        result = "윤년이 아닙니다."

    print(result)

print("연도를 입력하세요 :",end="")
year=int(input())

is_leap_year(year)


"""
윤년의 조건 :
그 해의 수가 4로 나누어 떨어지면 윤년이다. (예: 1996년은 윤년이다)
단, 그 해의 수가 100로 나누어 떨어지면 윤년이 아니다. (예: 1900년은 윤년이 아니다.)
단, 그 해의 수가 400으로 나누어 떨어지면 윤년이다. (예: 2000년은 윤년이다.)
"""